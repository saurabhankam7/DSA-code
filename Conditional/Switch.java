package Conditional;

/**
 * Switch
 */
public class Switch {

    public static void main(String[] args) {
        int a=4;
        switch (a) {
            case 1:
                System.out.println("mon");
                break;
            case 2:
                System.out.println("tues");
                break;
            case 3:
                System.out.println("wed");
                break;
            case 4:
                System.out.println("thur");
                break;
            case 5:
                System.out.println("fri");
                break;
            case 6:
                System.out.println("sat");
                break;
            default:
                break;
        }
    }
}